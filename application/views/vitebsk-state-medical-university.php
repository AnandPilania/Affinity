<p>
There are 3 special Scientific Boards of the doctors and candidates' thesis defence on different specialties (parasitology, helminthology, drugs technology and pharmacy, pharmaceutical chemistry and pharmacognosy, internal surgery and gastroenterology). Nowadays the University scientists perform researches on 40 perspective medical projects. There are 45 scientific study groups of students. This society of young scientists is one of the famous in Belarus and the nearest countries. The magazines "Surgery News" and "Pharmacy News", "Immunology" are published by the University.
</p>
<p>
The overseas students are studying at the University since 1981. For this period of time more then 700 specialists graduated from Vitebsk State Medical University. They successfully work in different institutions and public health of 109 countries of the world. The overseas postgraduates continue to study at the clinical residency and research postgraduate training courses, 11 of them defended their thesis.</p>

<p><img src="<?= base_url()?>/assets/images/vitebsk-state-medical-university-3.jpg" class="img-left">The University develops different contacts in the realm of medical education, physicians training, scientific research, methodical and informational exchange with many institutions of Russian Federation and other countries. Republic centers "Infection in Surgery", Minimal Invasive Surgery, Lipid Center, Central Scientific Research Laboratory of the University are well known abroad. The University is the organizer of the international academic meetings, which stimulate active participation of all the scientists and lectures of the University in the activity of professional international societies and associations. Some of the well-known lecturers and scientists have been elected to the foreign Scientific Academies. There is a problem-based teaching, integration in the studying of the theoretical and clinical disciplines at the University. There is a rating estimation of the students knowledge. The Educational Methodical Center is coordinating the implementation of modern educational technologies. The specialists of the University have developed modern educational programs in order to train highly qualified medical specialists.</p>
<p>
