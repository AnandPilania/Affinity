<p><img src="<?= base_url()?>/assets/images/vitebsk-state-medical-university-6.jpg" class="img-left">Vitebsk State Medical University was founded in November 1934. The first Rector was Professor M. A. Hazanov. The foundation of Medical Faculty was finished by 1941. It included 32 theoretical and clinical departments. The Great Patriotic War broke the work of the Institute for several years. Many teachers and students participated in the defence of the country. The city and the Institute were almost in ruins. The rebuilding of the Institute was started in 1946. Years passed. Medical Institute was expanding; its clinical base was improving. The number of highly qualified specialists, professors and scientific workers was growing. In 1959, the Pharmaceutical Faculty was opened along with the Medical Faculty. A year later the Institute began to train specialists at the postgraduate study courses. In 1963 the Central Scientific Research Laboratory was opened in order to train scientists and highly qualified lecturers. During the last decades the Institute was developing very intensively. In 1984 the Physicians Advanced Training Faculty was established. In October 1993 the Ministry of Public Health of the Republic of Belarus resolved to transfer the Pharmaceutical Advanced Training Faculty to Vitebsk Medical Institute. In 1997 the Preliminary Training Faculty was established. Since September 1981 Vitebsk State Medical University started to train the overseas students on Preliminary Training, Medical and Pharmaceutical Faculties. The number of foreign students was increasing and in 1997 the Faculty of Overseas Students Training was opened. For the large merits in training physicians for public health of Asia, Africa, Near East and Latin America, the Vitebsk State Medical University was awarded with the Medal of Peoples Friendship by the Presidium of the Supreme Soviet of the USSR. In the beginning of 1999, according to the Republic of Belarus Ministry of Education Vitebsk State Medical University was accredited to a University status.</p>
<p>
The University was headed by famous scientists and physicians, talented organizers of public health A.N. Savchenko (1946-1949), I.I. Bogdanovich (1949-1962), G.A. Medvedeva (1962-1965), E.N. Medvedsky (1965-1979), M.G. Sachek (1979-1997). Since 1997 the Rector of Vitebsk State Medical University is a Corresponding Member of Belarus Academy of Medicine, Professor A. N. Kosinets.
</p>
<p>
At present there are 7 Faculties, 53 Departments, Central Scientific Research Laboratory, Scientific Medical Informational Department, Educational and Methodical Informational Center and Library.
