<p>In numerous laboratories with up-to-date diagnostic and research equipment, students attempt penetrating into the mysteries of structure and functioning of the human body and learn how to diagnose diseases and apply innovative methods of treatment. The members of the Medical faculty are actively implementing innovative educational programs aimed at teaching new medical technologies to medical staff.</p>

<p>Functioning at the faculty are: the Students’ scientific society that helps students become familiar with research from the beginning; the Young medical doctors’ community; the Students’ theatre the lecture center where lectures in literature, music and history are presented. The pre-university education is provided at the medical-biological school (preliminary courses).</p>

