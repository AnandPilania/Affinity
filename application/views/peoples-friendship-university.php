<h5 class="tabpane-head">PFUR today</h5>
<p>Today Peoples’ Friendship University of Russia is one of the leading state higher educational institutions of Russia. It is the only university in the world every year uniting students from 140 countries. The University has a multi profile structure of faculties and majors, typical for classical universities of the world. More than 77 thousand graduates of the University work in 170 countries, among them more than 5500 holders of PhD and Doctorate degrees. Specialists are prepared in 62 majors and lines of study. More than 29 thousand graduate and postgraduate students from 140 countries are currently studying at the university. They represent more than 450 nations and nationalities of the world. The University is a renowned research center. It has a team of 4,500 employees, among them 2,826 highly qualified teachers. The University intellectual fund comprises more than 870 author’s invention certificates and 150 patents of the Russian Federation in practically all fields of University scientific research.</p>
