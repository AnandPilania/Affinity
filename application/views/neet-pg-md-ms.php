<h4 class="tabpane-head1 text-center">About NEET PG 2020</h4>
<p>NEET PG is an entrance exam to get MD MS admission courses and different PG diploma courses. This exam is conducted by the National Board of Examination (NBE) this year on January 5. The NBE has declared the exam results on 30 January. The NEET PG 2020 aspirants can download their scorecard by using ID and Password by the official website of NBE. Aspirants can also check the cutoff list here according to their categories. However, the required percentiles for different categories are different. For the General Category aspirants 360 score with a percentile of at least 50 is required and for ST/SC/OBC- 319 score is mandatory with a percentile of at least 40. The cut off marks are increased this year in the comparison of the year 2019.</p>

<p>The Authorities will soon make public the college list according to the merit list of NEET PG ( MD MS admission ) 2020 aspirants. The total number of seats offered by the Medical Counseling Committee (MCC) in Medical colleges are limited. These colleges can take admission on 10,601 MS seats and 19,525 seats for MD courses.</p>

<p>Here it is mandatory to mention that if a student has got the required marks then, it is not necessary that they will surely get admission. The cutoff mark is an eligibility criteria to sit in counseling program only. If seats get blank after the counseling then those seats will be offered to needy students. </p>
