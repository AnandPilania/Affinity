<h5 class="tabpane-head">History</h5>
<p><img src="<?= base_url()?>/assets/images/gomel-state-medical-university-1.jpg" class="img-left">On October 1, 1958, the Grodno State Medical Institute commenced academic studies (the first lecture was delivered by Prof. D.M.Golub from Minsk in Human Anatomy), and on October 15 the first sitting of the Academic Council took place. To the first Institute administration belonged L.F.Supron – Rector (1958-1962), N.I.Arinchin – Vice-Rector for Research and Academic Affairs (1959-1960), V.I.Garusin – Vice-Rector for Management and Administration (1958-1961), G.I.Kiriliuk – Dean (1958-1962). Over the following 36 years (from 1962 to 1998) the Institute was governed by Prof. Dmitri Maslakov. In 1964 the first 248 medical doctors graduated from the institute, 22 graduates were granted Honors Diplomas. At the beginning of the 1960s the first typical halls of residence were built, in the middle of the 1980s the first hall of residence of bearing-wall construction was put into service.</p>
<p>
Over the last 50 years, the Institute (and then the university) graduated over 16,000 physicians and graduate nurses. The Ministry of Health of the Republic of Belarus increased considerably enrollment in the University over recent years.
</p>
<p>
In the 1990s, new (and the unique in the Republic of Belarus at that) faculties were established: Faculty of Graduate Nurses (in 1991 – full-time department, in 1996 – part-time department) and Faculty of Mental Health Medicine (in 1993). Since 1992 foreign students have been trained at the University (at present our medical University ranks first among the institutions of higher learning in the city by the number of foreign students). Over recent years export of educational services has been significantly extended. In 2008 the Faculty of Medical Diagnostics was established with two majors: “Medical Diagnostics” and “Nursing”.</p>
<p>
<img src="<?= base_url()?>/assets/images/gomel-state-medical-university-2.jpg" class="img-left">The University has 5 faculties. The University offers five undergraduate majors, 33 majors for clinical residents, 20 majors for Ph.D. students. First master’s degree holders have been qualified.</p>

<p>In November 1999 – January 2000, the Institute has been successfully accredited as a University.</p>

<p>Major efforts are undertaken at the University to improve the quality of education. The “National Textbook” Project is being carried out at the University. Textbooks and manuals, which are prepared by the academic staff according to the specialization of the faculty, are published for medical students. Information technologies find a large scale application in the academic process. The computerization of the University has been accomplished, a PC-based library hall has been equipped, and information databases have been created. A three-level academic performance rating has been introduced. Grading criteria for all courses have been developed. Active forms of learning are used. Monitored independent students work is being introduced. Theoretical and practical clinical training of students is being upgraded. The students have the possibility for taking practical training in Poland and Germany.</p>

<p><img src="<?= base_url()?>/assets/images/gomel-state-medical-university-3.jpg" class="img-left">It is worthy of note that the University trains not only physicians but also researchers and the teaching staff for its own needs. At present the administration of the University consists mainly of the University graduates. By the number of persons with doctoral and Ph.D. degrees the Grodno State Medical University takes the leading position in the Republic of Belarus.</p>

<p>In 1960 the Central Scientific and Research Laboratory was opened. In 1970 the Metabolic Control Department was established under the auspices of the University, and in 1985 – the Institute of Biochemistry of the National Academy of Sciences of Belarus was founded. At present three Doctoral Councils are functioning at the University. The University works in close cooperation with academic and scientific centers in Russia, Ukraine, Lithuania, Poland, Germany. The Grodno State Medical University is a member of the Association of Baltic Universities.
</p>
<p>
Since 1993 a monthly “Aesculapius” paper has been published at the University. In 2003 the University started the publication of a quarterly medical journal. In 2000 the University Charter, Emblem and in 2006 – the University Anthem were approved.
</p>
<p>
In 2000 an honorable title “Doctor honoris causa of the Grodno State Medical University” has been instituted. It was conferred to Metropolitan Filaret and to prominent scientists from Belarus, Ukraine, Poland, Germany. The visitors of our University were conspicuous statesmen, prominent personalities of religion, science and culture, international ambassadors.
</p>
