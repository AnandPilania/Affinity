<p>The USMLE-Step 2-CK ("Clinical Knowledge") is the nine-hour long multiple-choice portion of the second part of the United States Medical Licensure Examination. It assesses clinical knowledge through a traditional, multiple-choice examination. In contrast to the USMLE Step 1, the focus is much more on clinical application of medical knowledge. It assesses the ability to apply medical knowledge, skills, and understanding of clinical science essential for the provision of patient care under supervision. The USMLE Step 2 is generally taken during the 4th year of medical school by medical students The subjects included in this exam are clinical sciences like Medicine, Surgery, Pediatrics, Psychiatry and Obstetrics & Gynecology.</p>
