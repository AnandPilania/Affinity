<p>The United States Medical Licensing Examination (USMLE) is a multi-part professional exam sponsored by the Federation of State Medical Boards (FSMB) and the National Board of Medical Examiners (NBME). Physicians with an M.D. degree are required to pass this examination before being permitted to practice medicine in the United States of America; see below for requirements of physicians with a D.O. degree.</p>

