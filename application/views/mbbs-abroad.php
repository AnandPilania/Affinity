<h5 class="tabpane-head">UK CLINICAL APTITUDE TEST</h5>
<p>The UK Clinical Aptitude Test (UKCAT) is part of the selection process of some UK medical and dental schools. It is an online test designed to test cognitive abilities, attitudes, critical thinking, and logical reasoning. There are four reasoning tests and a situational judgement test.</p>

<p>Universities are Public Universities so one can be sure about the acceptability and quality of education.</p>

<ul class="tab-panelist">
<li><strong>Verbal Reasoning.</strong> Assesses ability to think logically about written information and arrive at a reasoned conclusion: 21 minutes, with 11 passages to read and 44 questions.</li>
<li><strong>Quantitative reasoning.</strong> Assesses ability to solve numerical problems: 24 minutes, 9 tables, charts, graphs etc. as information and 36 questions.</li>
<li><strong>Abstract reasoning.</strong> Assesses ability to infer relationships from information by convergent and divergent thinking: 13 minutes and 55 questions.</li>
<li><strong>Decision Analysis.</strong> Assesses ability to deal with various forms of information to infer relationships, make informed judgements, and decide on an appropriate response: 32 minutes, 1 scenario full of information and 28 questions (basic calculator provided)</li>
<li><strong>Situational Judgement.</strong> Measures your responses in situations, and your grasp of medical ethics: 27 minutes and 67 questions on 20 scenarios.</li>
</ul>



<p>The test is taken at your local test centre, with each subtest in a multiple choice format. Past papers are not available but there are specimen questions on the UKCAT website. UKCAT must be taken by applicants at the following universities:</p>
