<h5 class="tabpane-head">PROGRAM STRUCTURE:</h5>
<p>MBBS in USA are structured such that once the students clear Step 1 of USMLE after finishing Basic Medical Sciences in Yr 2 (in some cases Yr 3) they could continue their 3rd and 4th year – Clinical studies (in few cases 4th and 5th years) in affiliated <strong>MBBS in USA </strong> (Optional). Students can do the clinical rotation in UK even without clearing PLAB/USMLE.</p>
<p>After finishing USMLE Step 1&2 with a decent score (Either in USA / UK or Central America), Students get a Temporary / Provisional License to pursue the Residency program in USA / Canada.</p>
<h4 class="tab-quote">“Residency” is an option for students to pursue Post Graduate training / Specialization in a teaching hospital in USA / Canada / UK, etc, on work based study mode. The PG program is 100% free of tuition fee. A very good stipend is paid during the program.<br>

Minimum Stipend ranges from US $ 3,500 and may be up to US $ 12,000 / Month. Students generally earn back what they have spent on their UG education within a span of 1 – 1.5 years. They also have very good chances of settling down in the countries from which the finish Residency.</h4>
<p>Further, Students who complete Residency program in USA / Canada / UK / Australia / New Zealand have additional benefits back in India too. They NEED NOT WRITE MCI SCREENING TEST IF THEY ARE COMING BACK TO INDIA TO CONTINUE THEIR PRACTICE.</p>
