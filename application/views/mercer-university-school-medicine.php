<h5 class="tabpane-head">History</h5>
<p>Mercer University was founded in 1833 in Penfield by Georgia Baptists. The school, under the leadership of Baptist minister and spiritual father Adiel Sherwood, was named for Jesse Mercer, a prominent Baptist leader and the first chair of the Mercer Board of Trustees.</p>

<p>Josiah Penfield gave the $2,500 that prompted the Georgia Baptist Convention to begin plans to open a school. Many Georgia Baptists gave matching funds for Penfield’s gift. The school opened under principal Billington Sanders.
</p>
<p>
Initially a boys' preparatory school named "Mercer Institute," the school at its founding consisted of a red clay farm and two hewed log cabins, valued at approximately $1,935. Enrollment for the first term was 39 students although, when the school opened, there were considerably fewer and others came over the first few weeks. Tuition was $35 for the year. Board was provided at $8 per month, and each student was required to supply his own bedding, candles and furniture.
</p>
<p>
From its humble beginnings in Penfield, Mercer today is a dynamic and comprehensive center of undergraduate, graduate and professional education. The University has more than 8,200 students; 11 schools and colleges – liberal arts, law, pharmacy, medicine, business, engineering, education, theology, music, nursing and continuing and professional studies; major campuses in Macon, Atlanta and Savannah; three regional academic centers around the state; a university press; two teaching hospitals; educational partnerships with Warner Robins Air Logistics Center in Warner Robins and Piedmont Healthcare in Atlanta; an engineering research center in Warner Robins; a performing arts center in Macon; and a NCAA Division I athletic program.
</p>
<p>
The institution’s reputation for exceptional academics in an engaged learning environment continues to grow. For almost two decades, U.S. News & World Report has ranked Mercer among the leading universities in the South. The Princeton Review repeatedly ranks it in the top 10 percent of all colleges and universities in North America. The University has been named a “College with a Conscience” by The Princeton Review and College Compact and has been named to the President’s Higher Education Community Service Honor Roll for distinguished community service. Mercer has also earned a Community Engagement classification by the Carnegie Foundation for the Advancement of Teaching.</p>
