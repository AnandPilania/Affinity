<p><img src="<?= base_url()?>/assets/images/mercer-university-school-medicine-1.png" class="img-left">Welcome to Mercer University School of Medicine (MUSM) with campuses located in Macon, Savannah and Columbus, Georgia. Mercer University School of Medicine (MUSM) is the graduate medical school of Mercer University. It was founded in Macon, GA in 1982, and as of 2008 has expanded to include an additional site in Savannah, GA, with either site allowing students to complete all four requisite years of medical training. In 2012, MUSM developed a third site in Columbus, GA at which students may elect to complete their third and fourth years of training. Mercer University School of Medicine adopts a mission that is heavily directed towards training future physicians to practice in the State of Georgia, and as such, prospective matriculants must be Georgia residents.</p>

<p>The Mission of the school is to educate physicians and health professionals to meet the primary care and health care needs of rural and medically underserved areas of Georgia. Mercer has been extremely successful fulfilling our Mission. As of October 2010, 1064 students have graduated from MUSM of those 288 are still in residency training programs. Currently there are 776 graduates in practice and 489 or 63% are practicing in the State of Georgia which is the best retention rate of any Georgia medical school. Greater than 70% of our graduates entered into primary care residencies. Based on data from the American Association of Family Physicians’ Robert Graham Center for Policy Studies in Family Medicine and Primary Care (2010) Mercer ranks 2nd in the nation for graduates practicing in low-income areas of the school’s home state; it ranks 3rd in the nation for graduates practicing in rural areas of home state and entering primary care in home state.</p>

<p>The Macon campus was founded in 1982 and is on the campus of Mercer University. The Savannah campus opened as a full four year campus in 2008 and is on the campus of Memorial University Medical Center. Students enrolled in the Doctor of Medicine program on both campuses experience the same curriculum. Preclinically, Mercer offers an integrated, clinically relevant problem-based medical education program that includes a longitudinal clinical skills curriculum a strong community medicine program and early patient care experiences. Such an academic environment fosters the early development of clinical problem-solving and instills in each student an appreciation of the importance of basic medical sciences in medical practice. Following their second year, students participate in core clinical clerkships at the School's primary teaching hospitals: Medical Center of Central Georgia in Macon, Memorial University Medical Center in Savannah, and The Medical Center and St. Francis Hospital in Columbus. Clinically, the curriculum is delivered in traditional clerkships utilizing both hospital and ambulatory settings. Students participate in a primary care preceptorship in communities throughout the state as part of the community medicine curriculum. In addition there is ample opportunity for students to participate in primary care and subspecialty clinical electives to broaden their educational experience. The small class size makes possible a close and collegial student/faculty relationship, individualized attention, and supportive atmosphere that enables every student to realize their potential for learning.</p>
