<div style="margin-bottom:30px;">
<h5 class="tabpane-head">Qualifications and experience of the applicant</h5>
<p>You must fulfil all of the following criteria:</p>
<ul class="tab-panelist">
<li>You must hold a primary medical qualification recognised by the GMC.</li>
<li>You need to have a postgraduate qualification (MRCP(UK) part 1, MD or other higher degree in medicine or a medical subspecialty.)</li>
<li>You must have completed at least 3 years of full-time postgraduate training (including 1 year’s internship and at least 1 year in the specialty in which you intend to train while in the UK).</li>
<li>You must have been in clinical practice for 3 out of the last 5 years including the 12 months prior to GMC registration being granted. There should be no gaps in employment. Please note that the GMC does not consider clinical observerships as clinical practice. It is important that you remain in clinical practice during the application process.</li>
<li>You must possess the skills, competencies and understanding of medicine at least equivalent to a UK graduate at the end of their CMT.</li>
</ul>
<p><strong>The RCP cannot sponsor doctors who have failed the Professional and Linguistic Assessments Board (PLAB) test.</strong></p>
</div>

<div style="margin-bottom:30px;">
<h5 class="tabpane-head">English language requirements</h5>
<p>You need to have obtained a minimum overall score of 7.5 in the International English Language Testing System (IELTS) examination, with minimum scores of 7.0 in all categories. The scores should be obtained in one sitting. The test is valid for 2 years.</p>
