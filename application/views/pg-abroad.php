<h5 class="tabpane-head">Medical Training Initiative (MTI)</h5>
<p>THE UK TRAINING PROGRAM </p>
<p>The Royal College of Physicians’ MTI scheme offers sponsorship for GMC registration and Tier 5 visa support to enable international medical graduates (IMGs) to access short-term training opportunities in the UK. The scheme aims to facilitate access to core medical or advanced specialty training and service experience for overseas doctors to improve the management and treatment of patients around the world.</p>
