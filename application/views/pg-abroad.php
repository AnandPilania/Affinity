<h4 class="tabpane-head">PLAB AND MRCP TRAINING</h4>
<div style="margin-bottom:30px;">
<h5 class="tabpane-head">Membership of Royal College of Physicians (MRCP)</h5>
<p>Membership of the Royal Colleges of Physicians of the United Kingdom (MRCP(UK)) is a postgraduate medical diploma in the United Kingdom (UK). The examinations are run by the Federation of the Medical Royal Colleges – the Royal College of Physicians of London, the Royal College of Physicians of Edinburgh, and the Royal College of Physicians and Surgeons of Glasgow. The three Royal Colleges of Physicians share this common three part assessment in general medicine which consists of two written parts and one clinical examination. Examinations are held throughout the UK and in overseas centres.</p>
<p>Holders of the MRCP(UK) can subscribe as “collegiate members” to any or all of the three UK Royal Colleges of Physicians. Thus the MRCP(UK) qualification has replaced the former MRCP(Lon), MRCP(E), and MRCP(G) qualifications.</p>
</div>

<div style="margin-bottom:30px;">
<h5 class="tabpane-head">Aim of Exam</h5>
<p>The exam incorporates both examination of the candidate’s knowledge of basic medical sciences as well as testing the clinical skills required for the diagnosis and management of disease. Changes to the exam in recent years have put more emphasis on communication skills and professionalism. Obtaining the “MRCP(UK)” is a prerequisite to anyone wishing to go on to a specialist training post as a Physician in the United Kingdom. Various companies, including the Royal Colleges themselves, have developed preparatory courses that focus on the nature of the questions and the required background knowledge.</p>
<p>In partnership with the relevant specialist societies, the three UK Royal Colleges have set up the MRCP Specialist Examinations Unit responsible for the organization of new knowledge based assessments. The ultimate objective is to ensure that NHS consultants have demonstrated their acquisition of sufficient knowledge in their chosen specialty to practice safely and competently. This will bring the assessment of physicians in training in the UK into line with those training in North America, where most specialist trainees sit a specialist examination as a further test of excellence, usually after having acquired certification in Internal Medicine.</p>
</div>
<div style="margin-bottom:30px;">
<h5 class="tabpane-head">UK Medical Training</h5>
<p><img src="images/MRCP training diagram.jpg" class="img-responsive"></p>
</div>
<div style="margin-bottom:30px;">
<h5 class="tabpane-head">Foundation Years</h5>
<p>The Foundation Programme is a two-year training programme for doctors who have just graduated from medical school.</p>
<p>It gives doctors in training experience in a range of different areas of medicine.</p>
<p>Doctors on the Foundation Programme are responsible for caring for patients under the supervision of more experienced doctors and other healthcare professionals.</p>
<p>After finishing the Foundation Programme, doctors choose an area of medicine to focus their training on. This may be a specialty or it may be general practice.</p>
<p>For the first year of the Foundation Programme (F1), doctors are provisionally registered with the GMC. During this year they must show that they have met the outcomes in Outcomes for provisionally registered doctors before they are eligible to apply for full registration.</p>
<p>In the second year of the Foundation Programme (F2) doctors are fully registered with the GMC. They still work under supervision but start to take on more responsibility for patient care.</p>

</div>
<div style="margin-bottom:30px;">
<h5 class="tabpane-head">Core Medical Training (CMT)</h5>
<p>The programme is overseen by the Joint Royal Colleges Postgraduate Training Board, which represents the three medical Royal Colleges in the UK: the Royal College of Physicians of London, the Royal College of Physicians of Edinburgh and the Royal College of Physicians and Surgeons of Glasgow.</p>
<p>Annual recruitment takes place centrally through a website-based application, followed by interviews held by regional Local Education and Training Boards.</p>
<p>Core medical training is one of the most popular medical specialties with over 2700 applicants applying for just over 1500 jobs each year. The ACCS (Acute Medicine) pathway also uses the core medical interview to recruit to ACCS.</p>
<p>CMT rotations characteristically comprise 4- or 6-month placements in various medical specialties. Part of the programme should include posts where the trainee is involved in the “acute medical take” (assessing patients referred for acute admission to hospital) and to become competent in acute scenarios and procedures.</p>
<p>CMT doctors are CT1 or CT2 (first or second year, respectively). At three points in the programme they undergo a progress assessment called Annual Review of Competence Progression (ARCP). CMT doctors are expected to complete the Membership of the Royal College of Physicians (MRCP) exam, without which it is not possible to enter specialist training.</p>
<p>Technically, since Modernising Medical Careers, CMT doctors are called specialty registrars, although the term is usually reserved for those who have completed MRCP and commenced subspecialty training.</p>
<p><a href="https://www.gmc-uk.org/Curriculum_for_Core_Medical_Training_2009__amended_2016_.pdf_66089593.pdf" target="_blank">Curriculum and assessment information for Core Medical Training.</a></p>
</div>
<div style="margin-bottom:30px;">
<h5 class="tabpane-head">Specialty Training</h5>
<p>In the UK medical system, a specialist is someone who has the necessary experience and qualifications to be placed on the GMC’s Specialist Register. Only persons on the Specialist Register can be appointed consultants in the National Health Service (NHS). Training to become a General Practitioner will also involve a Specialty Registrar training scheme and completion will lead to eligibility for entry on the General Practice Register.</p>
</div>

<div style="margin-bottom:30px;">
<h5 class="tabpane-head">Certificate of Completion of Training (CCT)</h5>
<p>It is a legal requirement that a doctor practising as a substantive, fixed term or honorary consultant in the NHS holds specialist registration and that a doctor practising as a GP in the UK holds GP registration. A CCT confirms that a doctor has completed an approved training programme in the UK and is eligible for entry onto the GP Register or the Specialist Register.</p>

<p>The GMC have approved and quality assured the training programmes that eligible CCT applicants have completed. This means an application for entry onto the Specialist or GP Register with a CCT is less complicated and quicker than other Specialist or GP Registration applications.</p>
</div>

<div style="margin-bottom:30px;">
<h5 class="tabpane-head">MRCP Exams</h5>
<p>Parts</p>
<p>The MRCP exam has three parts: MRCP Part 1 (written paper); MRCP Part 2 (written paper); and MRCP Part 2 Clinical Examination (PACES).</p>
<p>The <strong>MRCP part 1</strong> examination consists of multiple choice questions in the best of five format.</p>
<p>The <strong>MRCP part 2</strong> examination consists of multiple choice questions in the best of five format.</p>
<p>The <strong>MRCP PACES</strong> examination consists of a carousel with 5 stations.</p>
<ul class="tab-panelist">
<li><strong>Station 1.</strong> Tests the candidate’s ability to examine the respiratory system and abdomen.</li>
<li><strong>Station 2.</strong> Tests the candidate’s history taking ability.</li>
<li><strong>Station 3.</strong> Tests the candidate’s ability to examine the cardiovascular system and perform a neurological examination.</li>
<li><strong>Station 4.</strong> Tests the candidate’s communication skills and the ability to deal with issues of medical ethics.</li>
<li><strong>Station 5.</strong> Comprises two 8-minute “integrated clinical assessments” requiring the candidate to take a focused history and examination, formulate a differential diagnosis and management plan, and communicate the plan to the patient.</li>
</ul>
</div>

<div style="margin-bottom:30px;">
<h5 class="tabpane-head">Countries hosting PACES</h5>
<p>The MRCP PACES examination is held not only in the United Kingdom, but also in many other parts of the world. Hosting sites include Brunei, Dubai, Al Ain, Egypt, Kuwait, Oman, Qatar, India, Singapore, Malaysia, Malta, Hong Kong, Myanmar, Sri Lanka, and Sudan. Examination fees at centres outside the UK are significantly higher than at UK centres.</p>
<p>In Singapore, the MRCP(UK) and MMed are often taken together. In Hong Kong, MRCP(UK) is taken with MHKCP intermediate examination.</p>
</div>

