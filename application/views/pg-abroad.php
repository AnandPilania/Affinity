<h5 class="tabpane-head">Training Pathway</h5>
<p><img src="images/MRCP training diagram.jpg" class="img-responsive"></p>
 <p>Traditional route is the route, we all are very well aware of. It requires you to clear PLAB to join any PG program or speciality training in UK. In other words we can say that after clearing PLAB 1 and PLAB 2, you are entitled for GMC Registration. After GMC registration and required experience, you are required to take MRCP 1 to join Core Medical Training (CT 1 & CT 2). After two years of training one is required to clear MRCP 2 and MRCP Paces. It awards MRCP Diploma enabling you to take job in UK as a GP.</p>
<p>Clinical MD in internal Medicine is a new programme developed by the University  in conjunction with  NHS Foundation Trust, primarily aimed at the International Medical Graduates. In order to successfully complete this Clinical MD program, candidates are expected to have GMC registration supported by the Royal College of Physicians’ (RCP) Medical Training Initiative programme. Hands on clinical training will be provided by senior clinicians and medical educators.</p>
<p>As discussed earlier (MTI) we have discussed about the sponsorship by Royal College of Physicians (RCP) subject to fulfilment of conditions laid down by the sponsoring body enabling one to take Clinical MD or Speciality Training in UK without clearing PLAB 1 & PLAB 2.</p>
<p>The curriculum is linked to the e-Portfolio as it defines standards required for good medical practice laid out by the General Medical Council and a successful formal assessment at the end of each year placement. In order to obtain the degree candidates need to pass the Exit Exam along with successful completion of all the other modules and the MD dissertation. In conjunction with the Medical Training Initiative Programme by the Royal College of Physician, London, it was agreed that the NHS e-portfolios and the national Core Medical Training curriculum will be used to train and assess the trainees for the Clinical MD programme. Therefore, this curriculum was based in line with the RCP CMT curriculum under the direction of the Joint Royal Colleges of Physicians Training Board (JRCPTB).</p>
<p><strong>In brief, student directly joins CT1 & CT2 training (No need to take PLAB 1, PLAB 2 & MRCP 1) and after successful completion of two year training student is encouraged to take MRCP 2 and MRCP Paces (Full MRCP {UK}). Student is eligible to work in the concerned speciality and can also take Specialist Training (ST3 onwards).</strong></p>
