<h5 class="tabpane-head">PLAB Route</h5>
<p>The Professional and Linguistic Assessments Board (PLAB) test provides the main route for International Medical Graduates (IMGs) to demonstrate that they have the necessary skills and knowledge to practise medicine in the United Kingdom (UK). PLAB is a two part assessment that overseas doctors (or international medical graduates), from outside the European Economic Area and Switzerland, usually need to pass before they can legally practise medicine in the UK. It is conducted by the General Medical Council of the United Kingdom. The test is designed to assess the depth of knowledge and level of medical and communication skills possessed by the international medical graduates. The PLAB blueprint sets out what candidates are expected to demonstrate in the test and beyond.</p>
<p>The PLAB test has 2 parts:</p>
<ul class="tab-panelist">
<div class="img-right"><img src="images/pyramid-360x246.jpg" class="img-responsive"></div>
<li><strong>Part 1 :</strong> Consists of a multiple choice format examination paper with 200 SBA’s (Two Hundred Single Best Answer questions) lasting 3 hours, This part is conducted in a number of countries including Bangladesh, Egypt, India, Pakistan, Nigeria and Sri Lanka.</li>
<li><strong>Part 2 :</strong> Consists of an objective structured clinical examination (OSCE). This Part is available only in the city of Manchester, United Kingdom. It consists of 18 clinical stations. All the stations are eight minutes long, plus two minutes reading time. The standard of both parts of the PLAB exam is set at the level of competence of a doctor at the start of Foundation Year 2 (F2) in the Foundation Programme.</li>
</ul>
</div>

