<h5 class="tabpane-head">History</h5>
<p><img src="<?= base_url()?>assets/images/qingdao-university-2.jpg" class="img-left">The history of Qingdao University dates back to 1909. In 1908, Germany and representative of China, Zhang Zhidong Otto Frank negotiated to establish a joint college in Qingdao. As start up fund the Chinese govt. Invest 40% and Germen Govt Invest 60% of fund on September 12, 1909 German-Chinese college was officially opened. The First President was Georg Keiper, German Geologist and Navy Officer. In 1914, over 400 students were enrolled.</p>

<p>The medical department of German-Chinese College is the predecessor of the current Medical College of Qingdao University. Due to the war, German-Chinese College closed in 1914. However, despite numerous wounds of war, the medical department has never stopped. It was called Qingdao Medical College, Qingdao Medical College of East Asia, and several others in different periods. In 1922, in order to continue higher education, the Northern Government intends to set up a comprehensive university in Qingdao. In 1924, Private Qingdao University was founded.</p>

<p>In 1929, National Qingdao University was founded on the basis of Private Qingdao University, adding literature, education, mathematics, physics, chemistry, biology and other disciplines. In 1932, National Qingdao University was renamed as National Shandong University. In 1937, the Sino-Japanese War broke out. In 1938, the school was again closed. The war ended in 1945, and National Shandong University resumed in Qingdao, merging Qingdao Medical School and its affiliated hospitals.</p>

<p>In 1949, National Shandong University was renamed as Shandong University. In 1956, the School of Medicine was separated from Shandong University, and formed an independent establishment known as the Qingdao Medical College. 1958, Shandong University moved to Jinan. In 1978, Qingdao Medical College became the first master's degree granting units, professional settings from a single clinical professional to develop multi-disciplinary medical schools. Thereafter, Qingdao Medical College gradually developed more solid academic strength.</p>

<p>Qingdao University In 1985, Qingdao University was re-established, with the educational goal of "high level distinctive university," and disciplines of arts, science, engineering, business and many other subjects. School construction and development were supported financially by the central government, and also academically supported by Tianjin University, Nankai University, University of Science and Technology of China and Shandong University.</p>

<p>In 1993, Qingdao University merged Qingdao Medical college, Qingdao Normal College and Shandong Textile Engineering Institute. Qingdao University has established international academic relations, including exchange students, visiting scholars, academic cooperation, joint education, and Confucius Institutes, with over 170 institutions in more than 23 countries and regions. International exchange programs have been established with more than 40 universities and regions, including the USA, UK, Germany, Japan, Russia, Australia, Canada etc.</p>

