<p>The history of the RSMU began in 1906 when a medical faculty was opened as a part of Moscow Higher Women’s Courses. Later, in 1918, Moscow Higher Women’s Courses were reorganized and became the Second Moscow State University (2nd MSU) with a medical faculty. In 1930, as a result of the reorganization of the 2nd MSU , its medical faculty became an independent higher medical educational institution – the 2nd Moscow State Medical Institute (2nd MSMI) – the 2nd Moscow Order of Lenin State Medical Institute named after N.I. Pirogov (2nd MOLSMI). Since 1991 the Russian State Medical University has been the successor of the 2nd MOLSMI named after N.I. Pirogov. </p>

<p>Russian State Medical University offers a course of studies on medical and psychological major as well as on social work. One can get higher education at the university at the expense of the Russian Federation budget (free of charge if it's the first education being acquired), at the expense of Moscow government (free of charge with the obligation to work on Moscow Health Treatment department assignment after graduation in Moscow establishments for general health for 3 years) and also on a contract basis (charged).</p>

<p><img src="<?=base_url()?>assets/images/russia-1.png" class="img-left">The leading position of the RSMU in the national system of medical education is a convincing proof that the University has been demonstrating these qualities throughout its history.Russian State Medical University Another proof is a growing number of students and teachers, as well as an increasing number of faculties and departments. Moreover, the distinguishing feature of the University has always been its up-to-date attitude to vitally important demands of practical medicine and biomedical science. In 1963, during the period of active space exploration and development of new braches of science, the 2nd MSMI opened the first Medico-Biological Faculty in the world, which began to train researchers in the fields of biophysics, biochemistry and medical cybernetics. The actual demands of time dictated the establishment of new faculties, including the ones, which have no analogues in the world ( Pediatric, Medico biologic); the organization of faculties in response to the political situation (Military Faculty), to changing priorities in public health (Psychological-Social Faculty ) or to the introduction of a new concept of continuous professional education (Faculty of Additional Teachers’ Training , Faculty of Advanced Training of Doctors, Faculty of Preinstitutional Training). The very idea of the organization of a medical faculty in Moscow Higher Women’s Courses opened new prospects of increasing the number of doctors in Russia by recruiting women in the situation, when the country had a need in medical specialists.</p>

<p>During its existence the University has trained more than 70,000 doctors who fulfilled and continue to fulfill their professional duty in all parts of our country and abroad. In 1966 the University was awarded the highest order of the country - Order of Lenin - for its achievements in training medical specialists and on the occasion of its 60th anniversary.</p>

<p>Faculty of advanced medical studies (FAMS) of RSMU was opened in 1977. It was the first in the USSR and in RSFSR faculty of advanced medical studies at the Medical University.</p>

