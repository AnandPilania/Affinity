<p>The Faculty of Medicine of the University of Oradea, which was founded in 1991, is located in Piata 1 Decembrie 10. It has two sections, one for the Romanian and one for the international students. The international section which has over 500 students from all over the world gives the ability to foreign students to study medicine, in courses taught entirely in the English language. The Faculty has a 6 years curriculum, and the languages of instruction are Romanian and English.</p>

