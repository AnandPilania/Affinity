<p>After an initiative of the Dean of the Faculty of Medicine Professor Dr. Gheorghe Bumbu, the Faculty of Medicine of the University of Oradea is following the educational method which was first applied by Leiden University, in which medical students are given the ability to present the topics of their lectures, supervised by their professors.</p>

<p><strong>A Brief Overview of Romanian Faculties of Medicine</strong><br>
Romanian medical universities offer quality education for students interested in pursuing a career in Medicine. Most of the English Medical Departments were opened in the early 90s and have since produced 1000s Medical Doctors from countries all around the world. The medical qualifications are highly recognized and graduates write the same state licensing exams as the Romanian language departments. The majorities of Romanian International Medical Universities are non-profit and is owned by the state, although they maintain autonomy in their management.</p>
