<h5 class="tabpane-head">Conferences</h5>
<p><strong>Medical Days of Oradea</strong><br>
Medical Days of Oradea is the annual congress organised by the Faculty of Medicine and Pharmacy of the University of Oradea. During the Medical Days of Oradea, doctors and medical students from all over the country, have the opportunity to present the outcomes of their research.</p>

<p><strong>Search and Rescue</strong><br>
Search and Rescue, is the annual conference of Emergency Medicine and Rescuing in Special Situations, organised by the Faculty of Medicine of the University of Oradea, S.A.R.T.I.S.S., S.M.U.R.D. and Salvamont Rescue Team.
</p>

<p><strong>Research</strong><br>
Biobaza, the newly founded research center of the Faculty of Medicine and Pharmacy, is located in the center of the city.
</p>

<p><strong>Publications</strong><br>
The Faculty of Medicine of the University of Oradea, is publishing the Oradea Medical Journal. Oradea Medical Journal, is an interdisciplinary, medical journal focusing in the continuing education of primary health care providers all around the world. Its main aim, is to assist the practising physicians to improve their diagnostic abilities in their day to day practise, by providing them the access to quality review articles, as well as original research papers.</p>

<p><strong>Activities</strong><br>
Various activities are part of the Academic Curriculum. A Freshmans Party which is organised annually by the Faculty of Medicine and Pharmacy, is the traditional event welcoming the new students.<br>
The International Evening, organised by the Faculty of Medicine and Pharmacy, is one of the most spectacular events of the Academic Year. During the event, students from all over the world offer a traditional dinner, while giving the opportunity to their guests, to have a small impression of their cultures and traditions through presentations and musical shows.<br>
The Spring Barbeque, taking place on the first Friday of May, is occurring in parallel with a medical books market, as well as various other competitions. 
</p>

<p><strong>Summer Camping</strong><br>
Medical Adventure School on Holidays (MASH), is an annual summer training camping for medical students that started in 2001. It is offering courses of First Aid, trainings for rescuing in special situations, as well as various competitions and team tasks, to medical students from all over Europe.</p>
<p>MASH is organised by S.A.R.T.I.S.S. and the Association of Medical Students of the University of Oradea. The Faculty of Medicine of the University of Oradea, Salvamont Rescue Team, the Romanian Red Cross and S.M.U.R.D. of Bihor County, are some of the co-organizers.</p>

