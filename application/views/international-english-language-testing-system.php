<h4 class="tabpane-head1">International English Language Testing System (IELTS)</h4>
<div class="row" style="margin-bottom:30px;">
<div class="col-md-12">
<p>IELTS is the International English Language Testing System which tests English proficiency across the globe. IELTS is the world’s most popular high stakes English language proficiency test.</p>

<p>IELTS is accepted by more than 7,000 organizations worldwide. These include universities, immigration departments, government agencies, professional bodies and multinational companies. IELTS is available on 48 fixed dates a year – up to four times a month, depending on local demand.</p>

<p>IELTS is jointly owned by British Council, IDP: IELTS Australia and Cambridge English Language Assessment and offered through over 800 test centers and locations in over 130 countries. International teams of writers contribute to IELTS test materials. Ongoing research ensures that IELTS remains fair and unbiased. Test writers from different English-speaking countries develop IELTS content so it reflects real-life situations.</p>

<p>IELTS has two versions – Academic and General Training. The Academic test is for those who want to study at a tertiary level in an English-speaking country. The General Training test is for those who want to do work experience or training programs, secondary school or migrate to an English-speaking country. All candidates take the same Listening and Speaking tests but different Reading and Writing tests.
</p>
<p>
IELTS has four parts – Listening (30 minutes), Reading (60 minutes), Writing (60 minutes) and Speaking (11–14 minutes). The total test time is 2 hours and 45 minutes. The Listening, Reading and Writing tests are done in one sitting. The Speaking test may be on the same day or up to seven days before or after the other tests.
</p>
<p>
There is no pass or fail in IELTS. Candidates are graded on their performance in the test, using scores from 1 to 9 for each part of the test – Listening, Reading, Writing and Speaking. Your results from the four parts then produce an overall Band Score.</p>
