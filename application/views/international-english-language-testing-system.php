<h5 class="tabpane-head">How do I register for the test?</h5>
<ul class="tab-panelist">
<li>Find your nearest IELTS centre and check the test dates to find two options that suit you. Note the deadlinefor registering for each test date. (You can also see the cost of the test at your centre in local currency on the IELTS Worldwide Search.)</li>

<li>Check with your organization or on the Global Recognition System whether you need to sit an Academic or General Training test.</li>
<li>Print out the IELTS Application Form or ask your test centre for a copy. Read the information for candidates, terms and conditions complete the form and sign it. Please make sure you have a valid postal address and use the same name as on your passport.</li>

<li>Organize two identical passport-sized color photographs, less than six months old, and sign the back of each. You must not wear glasses in your application photographs. Take a photocopy of your current passport – this must be valid and not expired (some centers will accept a national identity card).</li>
<li>
If you want your results to be sent automatically to a university or educational institution, include the correct details in the section on the IELTS Application Form.</li>
<li>Take your application into your IELTS test centre with your money. If you send it by mail, please talk to your centre about the method of payment.</li>
<li>Once you have registered, the test centre will confirm your IELTS test date, time and venue. Please note the Speaking test can be up to 7 days before or after the test date.</li>
<li>On the test day, you must bring the same passport or national identity card that you entered on the Application Form.</li>
