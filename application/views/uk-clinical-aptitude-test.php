<p>The UK Clinical Aptitude Test (UKCAT) is a test that is beginning to be used in the selection process by a consortium of UK University Medical and Dental Schools. It is run by the UKCAT Consortium in partnership with Pearson VUE. It was first introduced in 2006, and will remain in test mode for some years beyond this date. The test is designed to give information on the candidates' mental abilities, as well as attitudes and possible professional behavior. The test is expected to start helping universities make more informed choices between medical and dental applicants in the years to come, once the test has been fully validated. Candidates intending to apply in 2013 for entry to a relevant course in a UKCAT University in 2014 (or for deferred entry in 2015) are required to sit the UKCAT by the 4th October 2013 deadline.</p>

