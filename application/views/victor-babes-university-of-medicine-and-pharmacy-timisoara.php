<p><strong>Section 1<br>
Preparatory year</strong><p>
<p><strong>Art. 4.</strong> – Foreign citizens from EU third countries will be registered for university and post-graduate studies only after graduating the Romanian language preparatory year or on the basis of a certificate proving Romanian linguistic competencies; during the preparatory year, the candidates will learn the necessary Romanian language vocabulary as well as the specific vocabulary needed for the future studies (anatomy, physics, chemistry, technical drawing etc.), if the study program is held in Romanian.</p>

<p><strong>Art. 5.</strong> – The duration of the preparatory course for pre-university and university bachelor degree studies is of one school/academic year, while for the master’s degree studies, PhD. studies and post-graduate studies in residency is of at least 6 months.</p>

<p><strong>Art. 6.</strong> – The accredited high education institutions which organize preparatory courses for learning Romanian are stipulated in Annex no. 1, which is part of the present Methodology.
</p>

<p><strong>Art. 7.</strong> – When registering for study programs that require Romanian linguistic competencies, the following categories of persons are exempt from the obligation to present the certificate of graduation of the Romanian language preparatory year:
</p>

<ul class="tab-panelist">
<li>persons who present Romanian study documents (diplomas and certificates) or study documents which prove at least four consecutive years of study in a pre-university school or at least three consecutive years of study in an accredited high education institution within the Romanian national system;</li>
<li>Persons who pass the Romanian language test following the examination conducted by a specialized commission formed of teachers that have at least the academic rank of lecturer. The examination can be done within the high education institution where the candidate applies if this high education institution carries on a temporary authorized or acknowledged specialization/ study program for Romanian language and literature, or within other accredited high education institution that develops such a specialization/ study program. Following this examination, a certificate of language proficiency will be issued.</li>
</ul>
