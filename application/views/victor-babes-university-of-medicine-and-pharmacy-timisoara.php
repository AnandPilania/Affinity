<li>the results obtained during pre-university/university studies;</li>
<li>the candidate’s academic activity in the area that was chosen for studies;</li>
<li>the schooling capacity of the accredited high education institutions;</li>
<li>Other criteria imposed by universities in their own methodology.</li>
