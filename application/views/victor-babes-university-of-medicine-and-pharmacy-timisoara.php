<h4 class="tabpane-head1">Victor babes university of Medicine and Pharmacy</h4>
<div class="row" style="margin-bottom:30px;">
<div class="col-md-12">
<p>The Victor Babes University of Medicine and Pharmacy is located in Timisoara, Romania.</p>
<h5 class="tabpane-head">History</h5>
<p><img src="<?= base_url()?>/assets/images/victor-babes-university-of-medicine-and-pharmacy-timisoara-1.png" class="img-left">The school was founded as a modest faculty on 30 December 1944, when King Michael I of Romania signed decree-law nr. 660". Art. 1. - The West University is founded on the date of publication of this hereby law, having the following faculties: Law, Literature and Philosophy, Sciences, Human medicine, Veterinary medicine, Pharmacology and Theology. Victor Babes University of Medicine and Pharmacy, Timisoara "The inauguration of the new university took place on 15 July 1945 and classes started 3 days later in the Notre Dame building (today the Students' House) on Anatomy, Biochemistry, Physiology, Physiopathology, Histology, Microbiology and Parasitology.</p>

<p>There were 412 students enrolled in the first year and 76 students in the second year, transferred from other universities, compared with 4237 students in the academic year 2000-2001. The first class graduated in 1949, counting 70 graduate students.</p>

<p>Since the school opened, 24,366 graduates, of whom 4,284 foreigners have successfully completed their medical studies at the University of Timisoara . The first foreigner to study there did so in 1967-1968, when a single student from Yemen attended.</p>

<p>At its inception, the building of the school was used for student lodging but eventually that purpose was served by the top floor of the university. Currently, the university has at its disposal 8 hostels, placed either in the close proximity of the university, or in the university campus. Meals were served at the Territorial Military Center and then at the canteen of the Polytechnic Institute until October 1945, when the university founded its own canteen, which today is able to provide meals for 900 people per day. Furthermore, in October 1945 the "Banatia" building, which had suffered considerable damage during World War II, was acquired, but this did not hinder the enthusiasm of the students of those days, and through considerable voluntary efforts contributed to a minimum organization of the building. Today, the university comprises a total of 8 buildings (the former "Banatia" being the main quarters of the university) in which different disciplines carry on their specific everyday activities.</p>

<p>The clinical teaching started initially in 4 hospitals, but today, students are being trained in more than 25 university clinics, belonging to 12 hospitals. In time, the evolution of the University was also marked by changes in its title:</p>
<ul class="tab-panelist">
<li>until 1948 - the Faculty of Medicine;</li>
<li>between 1948-1990 - The Institute of Medicine, which immediately after 1948 included three faculties: General Medicine, Pediatrics and Hygiene</li>
<li>after 1990 - the University of Medicine and Pharmacy</li>
<li>since 2001 - Victor Babes University of Medicine and Pharmacy</li>
</ul>
<p><img src="<?= base_url()?>/assets/images/victor-babes-university-of-medicine-and-pharmacy-timisoara-2.png" class="img-left">Victor Babes University of Medicine and Pharmacy, TimisoaraThe modest Faculty of Medicine developed and matured in spite of all difficulties it faced throughout time. Today, the Victor Babes University of Medicine and Pharmacy is a prestigious institution of higher education, with a past that honours and a promising future that will for sure bring valuable results both in preparing new generations of young physicians, pharmacists and dentists and also in the medical assistance offered to the community.</p>

<p><strong>Faculty of Medicine</strong><br>
Since 1944, when His Majesty King Michael I of Romania issued a Decree-Law by which the Western University of Timisoara was founded, over 15,000 physicians, more than 3,000 of which foreigners, have graduated our university.</p>

<p>A university lives through its students and its teaching staff and it acquires fame through its academic environment and through the results it can show the world. One of the main goals of the Faculty of Medicine is to train competent and skilled physicians. This goal implies thorough knowledge of general medicine, one of the most difficult medical specialties, combining clinical aspects with fundamental research. Starting from these premises, all the subjects studied in the Faculty of Medicine are considered equally important.</p>

<p>The Faculty of Medicine has the following specializations:</p>
<ul class="tab-panelist">
<li>Bachelor in Medicine – 6 years, full-time attendance</li>
<li>Bachelor in Nursing (Registered nurses) – 3 years, full-time attendance</li>
<li>Bachelor in Nursing for Imaging and Radiology – 3 years, full-time attendance</li>
<li>Bachelor in Clinical Laboratory Nursing – 3 years, full-time attendance</li>
<li>Bachelor in Nursing for Balneo-physiotherapy and Recuperation – 3 years, full-time attendance</li>
<li>Bachelor in Nursing for Nutrition and Dietetics - 3 years, full-time attendance</li>
</ul>

<p><img src="<?= base_url()?>/assets/images/victor-babes-university-of-medicine-and-pharmacy-timisoara-3.png" class="img-left">For the specialization General Medicine, the first three years are dedicated to preclinical training, and the last three years to clinical training. The preclinical cycle involves the study of fundamental subjects: anatomy, histology, biology, biochemistry, and biophysics. For the past years, preclinical training has been updated acquiring a more and more pronounced clinical character, by correlating the information from preclinical subjects with their application into medical practice, and by introducing in the curriculum subjects such as NEUROSCIENCES and FIRST AID.</p>

<p>The clinical cycle consists in clinical subjects with a pronounced medical and surgical character, as well as subjects of preventive medicine. The curriculum is completed with complementary subjects, such as the history of medicine, social sciences, sports, foreign languages, and Romanian for the foreign students.</p>

<p>The equipment of the teaching hospitals has been permanently updated, an example in this sense being the departments of cardiology, laparoscopic surgery, orthopedics surgery, gynecology and in-vitro fertilization, pediatrics and urology.</p>

<p>One of the important developments for the Faculty of Medicine lies in the numerous international contacts it has established in the past years. Thus, the faculty’s cooperation with prestigious European universities (and not only) makes possible the implementation of short- and medium-term programmes, which contribute to definitely enhancing the quality of the training process.</p>

<p>Research plays a major role in the academic activity of the faculty. Focusing on the two research directions: fundamental and clinical, undergraduate, post-graduate, doctoral and post-doctoral students are encouraged to access scientific network and publish the results of their research activities in international medical journals. To familiarize undergraduate students with the requirements of scientific work and to make them understands the major importance of research in medical activity; the faculty has recently introduced in its curriculum the subject “Methodology of the Scientific Research”.</p>

