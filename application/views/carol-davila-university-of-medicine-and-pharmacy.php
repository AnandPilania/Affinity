<h5 class="tabpane-head">History</h5>
<p><img src="<?= base_url()?>/assets/images/carol-davila-university-of-medicine-and-pharmacy-1.png" class="img-left">The higher medical and pharmaceutical education in Bucharest dates back more than a century. Carol Davila, a Romanian physician of French origin, in collaboration with Nicholae Kretzulescu founded the medical education in our country, by establishing in 1857 the National School of Medicine and Pharmacy. Thanks to his activity a number of scientific societies were born in our country such as "The Medical Society" (1857), "The Red Cross Society” (1876), "The Natural Sciences Society” (1876), and two medical journals: "The Medical Monitor" (1862) and "The Medical Gazette" (1865). In the war for independence (1877-1878) he led the army's military service.</p>

<p>The building of the Faculty of Medicine was fully completed and inaugurated on the 12th of October 1903 when was inaugurated also of the statue of Carol Davila, seated in front of the faculty. The initiative to erect a monument to Davila was taken at the first national medical conference held in Bucharest in October 1884. The statue, valued work of Carol Storck, was cast in bronze in the School of arts and crafts workshops in Bucharest.
</p>
<p>
The inauguration of the faculty building is an important date in the evolution of medical education in Bucharest. The new building brought great improvements in the functioning of laboratories and the organization of practical work, as well as in the full didactic activity. He sheltered for a while also a part of the pharmacy department.
