<p>The university is active in promoting cooperation and exchanges with other countries. It has established links with Australia, Austria, Canada, Finland, France, Germany, Japan, Spain, Switzerland, UK and USA in the fields of education, science, technology and economics. A number of international joint programs have been established between the university and its counterparts in other countries in recent years. In 2006, the university enrolled 1,829 international students.</p>
