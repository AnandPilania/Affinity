<h5 class="tabpane-head">History</h5>
<p><img src="<?= base_url()?>assets/images/tongji-university-2.jpg" class="img-left">The history of Tongji University can be traced back to 1907 when the German Medical School for the Chinese in Shanghai was founded by the German government together with the German physicians Erich Paulun, Oscar von Schab and Paul Krieg. The school was affiliated with the Tongji-Hospital the German physicians had established in Shanghai on the initiative of Paulun. The name Tongji suggests cooperating by riding the same boat. The school was expanded to include engineering in its programs and got its new name as German Medical and Engineering School for the Chinese in Shanghai in 1912. It was formally established as a Chinese university under the name of Tongji University in 1923/1924 and was renamed as National Tongji University in 1927. During the Second Sino-Japanese War (1937–1945), the university campus was moved from Shanghai first to Zhejiang Province, then to Jiangxi Province and Yunnan Province and later to Sichuan Province. It was eventually moved back to Shanghai in 1946. It then grew to be a comprehensive university which offered programs in science, engineering, medicine, arts, and law. Following a nationwide campaign of reorganizing schools and departments between universities in 1952, Tongji University became a university with strength in engineering, particularly in Civil Engineering. Tongji is also the first university that introduced Urban Planning into China.</p>
<p>As a university which had established a reputation for its research, Tongji became one of the first batch of universities which were authorized by the China State Council to establish its Graduate School. As one of the leading universities, it was successful in its application for the 211 Program which provided universities with substantial government fund. In 1995, the university became one to be jointly supported by the State Education Commission and the Shanghai Municipal Government. In 1996 the university merged with Shanghai Institute of Urban Construction and Shanghai Institute of Building Materials. The merger was acknowledged by the State Council as "Tongji Model" in the system renovation of higher institutions in China. In April 2000, the expanded Tongji merged again with Shanghai Railway University. Now Tongji University has become a comprehensive university which offers a wide range of programs in science, engineering, medicine, arts, law, economics and management.</p>
