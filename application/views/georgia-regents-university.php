<h5 class="tabpane-head">History</h5>
<p><img src="<?= base_url()?>/assets/images/georgia-regents-university-1.png" class="img-left">The university was chartered in 1828 by the state of Georgia as the Medical Academy of Georgia to offer a single course of lectures leading to a bachelor's degree. In December 1829, the Georgia General Assembly changed the name to the Medical Institute of Georgia, and again to the Medical College of Georgia in December 1833. The College of Nursing offered its first classes in the 1940s. In the 1960s, Master's and doctoral programs were added. The Colleges of Allied Health Sciences, Dental Medicine and Graduate Studies were added in the next decade.</p>

<p>The Medical College of Georgia is the 13th-oldest continuously operating medical school in the United States and the third-oldest in the Southeast.</p>

<p>Recent facilities expansion includes the Cancer Research Building, opened in 2003; the Health Sciences Building in 2006; and a new dental school building in 2011. On Feb. 1, 2011, the school’s name changed to Georgia Health Sciences University before consolidating with Augusta State University to become GRU. On August 22, 2012, Regent University announced its intentions to file a lawsuit for a trademark dispute against the Board of Regents of the University System of Georgia over the name of Georgia Regents University, the adopted name of the consolidation between Augusta State University and Georgia Health Sciences University, stating that the name infringes on the Regent University trademark and will create marketplace confusion; however, the USG Board of Regents contend that the name is broad enough where Regent's lawsuit would be dismissed. The Georgia Regents name is unpopular with students and alumni within the two Augusta institutions as well as the Augusta community at-large; those protesting against the name hoped that the lawsuit would force the University System to reconsider the name. Additionally, attorneys based in Augusta attempted to contact Regent University about assisting in the lawsuit; however, the university stated that they will use their regular trademark counsel. On October 22, 2012, Georgia Attorney General Samuel Olens filed motions in federal court to have Regent University's lawsuit dismissed, citing sovereign immunity as well as the broadness of the "Regent" name as the basis for dismissal.</p>

