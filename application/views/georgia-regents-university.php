<p><img src="<?= base_url()?>/assets/images/georgia-regents-university-3.png" class="img-left">The campus was originally used as a Confederate Army arsenal during the Civil War and included a massive powder works to supply gunpowder for the Confederate Army. Following the surrender, the arsenal fell into more of a support role, beginning with the Spanish-American War in that the arsenal produced manufacturing equipment, seacoast targets, and was a repair station. In World War One, the station repaired rifles and small arms, but produced ordnance material and fire control operations for World War Two. In 1955, the arsenal was closed, and two years later the land was given to the local Board of Education, which used it to open the Junior College of Augusta. In 1958, the name changed to Augusta College, and in 2001 to Augusta State University.</p>
