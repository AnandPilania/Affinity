<p>Georgia Regents University (GRU) is a public academic health center with its main campus located in Augusta, Georgia, United States. It one of the four research universities in the University System of Georgia (USG). GRU is composed of nine colleges: the College of Allied Health Sciences, College of Arts, Humanities, and Health Sciences, College of Dental Medicine, College of Education, College of Graduate Studies, College of Nursing, College of Science and Mathematics, the Hull College of Business, and the Medical College of Georgia. On January 8, 2012, the University System of Georgia Board of Regents approved the merger of Georgia Health Sciences University with Augusta State University. On August 7, 2012, the Georgia Board of Regents named the merged universities "Georgia Regents University".On October 25, 2012, the university added the city's name to the university name for marketing purposes, which is Georgia Regents University Augusta. In addition to the nine colleges, the university enterprise includes the 478-bed Georgia Regents Medical Center, the Children's Hospital of Georgia, outpatient clinics, classrooms, laboratories, residence halls, a student center, a wellness center and a medical education library. The entire complex has a full-time instructional faculty of 651, a volunteer clinical faculty of 1,795 and a staff of over 3,000, making it the second-largest employer in the region with an annual economic impact of $2 billion. The university receives over $99 million annually in total sponsored research funding.</p>
