<h5 class="tabpane-head">About DTMU</h5>
<p><img src="<?= base_url()?>/assets/images/aieti-medical-school-4.png" class="img-left">Among numerous factors contributing to medical progress, an important role is assigned to perfection of the system of continued medical education and training of the personnel for high qualification. In the 1990s, the Georgian Medical Postgraduate foundation was established in Tbilisi, Georgia (Dr. Dm. Tvildiani), which enrolled over 200 young physicians aimed at passing the US Medical Licensing Examination(operating in more than 80 countries) with subsequent completion of medical residency program in the USA or other countries. The foundation effectively provided the members with all necessary programs, textbooks and other aids. However, it turned out next to impossible to master the program independently, with only few students being able to pass. This caused an urgent need to found a special, radically new school of higher medical education.</p>
<p>In 1992/1993, the David Tvildiani Medical University (DTMU) started functioning under the patronage of Acad. David Tvildiani and Dr. Dimitri Tvildiani. It is one of the first non-governmental higher medical education institutions in Georgia. Since 2011 David Tvildiani Medical University (DTMU) was renamed after the academician David Tvildiani in David Tvildiani Medical University. The university has one faculty - the Faculty of General Medicine. The duration of the educational process lasts for 6 years. It consists of three stages: Basic Medical Science (2.5 years), Clinical Science (2.5 years), Pre-diploma Specialization (Clinical Clerkship - one year). The curriculum consists of single cycles each concluded by a test which affects the final results obtained in a given discipline. The duration of an academic year makes up 40 weeks (2 semesters). The end of year examination is conducted in the form of test according to the quotas in all the subjects taken within the year. The quality of student’s knowledge is checked using the original self-designed computer-based assessment program including the complicacy number test questions. Learning of Basic Medical disciplines is system-oriented, while that of Clinical Medical Disciplines is Problem and System - oriented. According to the results of the course examination a student may have the fee discount. The final score is also influenced by the results of oral examinations taken throughout the year. Selection examinations are conducted for DTMU`s students and graduates aimed to pass the USMLE Steps. A student has to obtain the threshold exceeding score to be admitted as a candidate for the USMLE financed by the School.</p>
