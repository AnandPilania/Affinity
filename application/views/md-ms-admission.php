<p><strong>NEET PG 2020</strong> eligibility criteria as prescribed by the Medical Council of India (MCI) are mentioned here-under:</p>

<ul>
	<li>Candidates must possess MBBS degree certificate or Provisional MBBS Pass Certificate.</li>
	<li>Candidates must have completed one year of internship or are likely to complete their internship on or before 31 March of the year of PG medical admission.</li>
	<li>Candidates must possess Permanent/Provisional SMC/MCI Registration Certificate.</li>
	<li>Foreign Medical Graduates (Indian Nationals or OCIs) must have qualified Foreign Medical Graduate Examination (FMGE) and registered with SMC/MCI.</li>
	<li>Foreign Nationals are eligible without MCI registration. The MCI may grant temporary registration, on payment of prescribed fee, to such candidates for the duration of their PG study, provided they are duly registered with the concerned authority in their own country from where they have obtained the primary medical qualification.</li>
</ul>

