<p><img src="<?= base_url()?>/assets/images/huazhong-university-of-science-and-technology-2.jpg" class="img-left">In June 1953, the land south of the East Lake was chosen as the campus of South-Central China Institute of Water Conservancy and Electric Power. The same year, the South-Central China government decided not to establish an independent South-Central China Institute of Power Engineering. All the programs related to power engineering were administered by Huazhong Institute of Technology. The institute formally opened on October 15, 1953.</p>

<p>All the programs and departments at HIT were organized and formed from the programs related to electrical engineering, power engineering, electricity and mechanical engineering in Wuhan University, Hunan University, Guangxi University and South China Institute of Technology. HIT's first classes were held in spaces at those institutions. There were eight undergraduate programs, 314 full-time faculty members and 2,639 undergraduate students at that time. </p>

<p>In 1971, most of the Wuhan Mechanical Institute was merged into the institute. Six departments were established. There were 20 undergraduate programs, 1,097 faculty members. The number of undergraduate students reached to 6,087.</p> 

<p>Graduate students began to be enrolled in 1978, and in 1984, the institute became one of 22 universities to be approved by the State Council to establish a graduate school. By approval of the State Education Committee, the institute was renamed Huazhong University of Science and Technology in 1988.</p> 

<p>With China's global strategy and the reform of the economy, HUST led a series of pivotal reforms among technical institutions in China. HUST is the first technical school to establish the Department of Journalism and Communication, the Department of Economics, and Department of Literature, and to host nation-wide lectures in humanities.</p>

<p>On May 26, 2000, the former Huazhong University of Science and Technology, Tongji Medical University , Wuhan Urban Construction Institute and Wuhan Science and Technology Vocational College combined to form the new Huazhong University of Science and Technology the Chinese name was changed, but the English name remained unaltered.</p>
