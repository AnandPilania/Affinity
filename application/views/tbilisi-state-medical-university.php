<h5 class="tabpane-head">History</h5>
<p>The University was solemnly opened on 8th of February, 1918 (old style 26th of January), the day of remembrance of the Georgian King David the Builder. Since that, this day remains one of the most significant events in the history of the nation.</p>

<p><img src="<?= base_url()?>/assets/images/tbilisi-state-medical-university-2.png" class="img-left">The founders of the Georgian University well understand the importance of introduction Higher Medical Education. The primary initiator of the university foundation, the reverend Professor Ivane Javakhishvili, had been speaking about the reasons of establishment of Medical Faculty “We still need to carry out research on the endemic flora and fauna in our country. Many human diseases have various characters and most are as yet unstudied. Consequently, scientific workers at our University’s Medical and Natural Sciences Faculties have a broad challenge area”.</p>
<p>The first Rector of University Professor Petre Melikishvili was an active supporter of medical faculty creation. On the meeting of “Georgian Physicians and Natural Sciences Society” (12th of October, 1917), he has expressed his opinion about the necessity of opening the medical and natural sciences faculty.</p>

<p>On February 27th, 1918, University Professors Faculty Council confirmed the exigency of creation of Medical Faculty. In June 17th, the same Council of Georgian doctors and Natural Scientists in the presence of the Chairman Spiridon Virsaladze decided to open Mathematics-Natural Sciences and Medical faculty.</p>

<p>In June 10, 1930 the Tbilisi State University was reorganized and Tbilisi State Medical University was founded. This has become a powerful stimulus of development Higher Medical Education and Medical Sciences in Georgia.</p>

<p>Tbilisi State Medical University at the foundation consisted of three faculties: Medical- Preventive (including Division of Stomatology), Sanitary- Preventive and Pharmacochemistry.</p>

<p>During the 80 years long history of the University in different periods of time were founded and functioning the following faculties: Pediatrics (since 1931), Stomatology (since 1936), Military Medicine (since 1993), Medical-Biology (since1994), psychosomatic medicine and Psychotherapy (since1996).</p>

<p>To date Tbilisi State Medical University has five faculties: Medicine, Stomatology, Pharmacy, Public health, Physical medicine and rehabilitation.</p>

<p>Tbilisi State Medical University runs four Higher Education Programs: General Practice Nurse, Midwifery, Medical Devices Technician, and Pharmacy Technician.</p>

<p>The analysis of objective, sometimes revolutionary, political and ideological changes in the country and Society that took place in the decades of the 90’s of the last century, revealed that for the achievement of University goals the establishment of larger space is necessary. The newly acquired autonomy would have been merged with new University structure that was the way for transformation of Medical Institute into University and for that the preconditions were already existed. The Author of this objective and well founded request was Tbilisi State Medical University itself.</p>

<p>24th of November 1992 is the historic day for Higher Medical Education in Georgia. The new stage was launched by the transformation of Tbilisi State Medical Institute into Tbilisi State Medical University.</p>

<p>Tbilisi State Medical University from the very beginning found itself in the forefront of overcoming the XXI century challenges of reforming medical education. In 1997 University has already developed a strategy of outcome-oriented teaching methodology implementation. The strategy has been developed on the bases of European experts recommendations. After all, University is a member of the Association of Medical European Education (AMEE), also a member of the World Federation for Medical Education (WFME) and cofounder of the. East –European Association of Medical Education (EEAME). University is not only According to above mentioned experts modern, competitive International Medical education considers the following main points: Critical thinking, Information management and Communicative skills. These are the basis for development of basic sciences and public health, professional values and relations.</p>

<p>By 2006, the necessity of implementation already existing Law on Higher Education of Georgia on the one hand, and the need of implementation full coverage of European and global advantages of medical education together with international commitments to reform the system of medical education on the other hand, have put the medical community to face the objective challenges.</p>

<p>The health care system with the old regulatory and financial mechanisms was poorly manageable. Accordingly, the traditional forms of training of qualified medical professionals required innovative changes. A three-stage system of higher education (baccalaureate, magistracy and doctorate) called for coordination with the process of post-graduate education. All this would make it possible to implement harmoniously the “life-long learning principles”, although it’s clear that the creation of such a continuum requires appropriate material and human resources.</p>

<p>All the aforesaid have caused a very significant step in Georgia's system of higher medical education - in 2006 Tbilisi State Medical Academy was integrated into Tbilisi State Medical University. The merging of these traditional medical schools in TSMU as one Medical Institution united also undergraduate and postgraduate medical education facilitated multilateral development of traditional medical and entire natural sciences.</p>

<p>By 2007, the faculty of Therapeutic Physical Training and Rehabilitation of the State Academy of Physical Culture and Sport joined TSMU. To this highly significant union was supplemented by competition for engagement in academic positions conducted in 2006 and 2007. As a result of the last competition there are 371 academic personnel: full professors, associate professors and assistant professors in TSMU. Besides them, 350 invited teachers are involved in academic process.</p>

<p>Tbilisi State Medical University collaborates with more than 40 Universities and centres; international scientific cooperation instigated to launch of numerous joint projects of International or National significance.</p>

<p>Nowadays TSMU has employed the highly intelligent, highly qualified pedagogical personnel and scientists. The University traditions, experience, reputation/authority and potential gives hope for its further successful development.</p>

